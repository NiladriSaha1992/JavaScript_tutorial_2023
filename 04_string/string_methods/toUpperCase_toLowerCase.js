/*
  string.toUpperCase() ->
    This string method converts all the alphabets of a string to uppercase.
*/

const firstName = "Niladri";
const lastName = "Saha";
const address = "48M/13 Rabindra Sarani, Liluah, Howrah, 711204";

console.log(`Full Name: ${firstName.toUpperCase()} ${lastName.toUpperCase()}\nAddress: ${address.toUpperCase()}`);

/*
  string.toLowerCase() ->
    This string method converts all the alphabets of a string to lowercase.
*/

const email = "SAHANILADRI25Y@GMAIL.COM";
const password = "Nil123";

console.log(`Email Id: ${email.toLowerCase()}\nPassword: ${password.toLowerCase()}`);